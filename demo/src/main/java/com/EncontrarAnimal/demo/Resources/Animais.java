package com.EncontrarAnimal.demo.Resources;

public class Animais {
	
	public String tipoAnimal;
	public String raca;
	public String cor;
	public String bairro;
	public String descricaoAdicional;
	
	public String getTipoAnimal() {
		return tipoAnimal;
	}
	public void setTipoAnimal(String tipoAnimal) {
		this.tipoAnimal = tipoAnimal;
	}
	public String getRaca() {
		return raca;
	}
	public void setRaca(String raca) {
		this.raca = raca;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getDescricaoAdicional() {
		return descricaoAdicional;
	}
	public void setDescricaoAdicional(String descricaoAdicional) {
		this.descricaoAdicional = descricaoAdicional;
	}
	
}
